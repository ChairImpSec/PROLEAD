void shift_table(uint8_t a,uint8_t Tp[][256],uint8_t T[][256])
{
  for(int j=0;j<2;j++)
    for(int k=0;k<256;k++)
      Tp[j][k]=T[j][k ^ a];
}

void refresh_table(uint8_t T[][256],uint8_t Tp[][256],int ind)
{
  for(int j=0;j<ind;j++)
  {
    for(int k=0;k<256;k++)
    {
      uint8_t tmp=xorshf96();
      T[j][k]=Tp[j][k] ^ tmp;
      T[ind][k]=T[ind][k] ^ tmp;
    }
  }
}

void read_htable(uint8_t a_share2,uint8_t* b_share1, uint8_t* b_share2, uint8_t T[][256])
{


  *b_share1 = T[0][a_share2];
  *b_share2 = T[1][a_share2];

  refresh(b_share1, b_share2);
}

void htable(uint8_t* share1, uint8_t* share2, int lam,uint8_t T[][256])
{
  uint8_t Tp[2][256];
  int i,k;
  
  for(i=0;i<lam;i++)
  {
    shift_table(*share1,Tp,T);

    for(k=0;k<256;k++)
      T[1][k]=Tp[1][k];

    refresh_table(T,Tp,1);
  }
}

