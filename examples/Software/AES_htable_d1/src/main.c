static unsigned long x=123456789, y=362436069, z=521288629;

unsigned long xorshf96(void) {   
  unsigned long t;

  x ^= x << 16;
  x ^= x >> 5;
  x ^= x << 1;

  t = x;
  x = y;
  y = z;
  z = t ^ x ^ y; 
  return z;
}

uint8_t multx(uint8_t x)
{
	uint8_t y = x;
	y = y << 1;
	// if((x & 0x80)!=0)  y=y ^ 0x1b;
	uint8_t m[] = {0, 0x1b};
	y ^= m[x >> 7];
	return y;
}
uint8_t subbyte(uint8_t x)
{
	return sbox[x];
}

void refresh(uint8_t* share1, uint8_t* share2)
{
  int i;
  for(i=1;i<2;i++)
  {
    uint8_t tmp=xorshf96();
    *share1 = (*share1) ^ tmp;
    *share2 = (*share2) ^ tmp;
  }
}

void share(uint8_t x,uint8_t a[])
{
  int i;
  a[0]=x;
  for(i=1;i<2;i++)
    a[i]=0;
}

uint8_t decode(uint8_t a[])
{
  int i;
  uint8_t r=0;
  for(i=0;i<2;i++)
    r^=a[i];
  return r;
}


